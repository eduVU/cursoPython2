import math

class Trig:
  def __init__(self):
    self.pi = math.pi  # atributo con el valor de pi basado en el módulo math.

  # Se crea el método para calcular sen(pi).
  def calcular_sen_pi(self):
    sen_pi = math.sin(self.pi)
    return sen_pi

  # Se crea el método para calcular cos(pi).
  def calcular_cos_pi(self):
    cos_pi = math.cos(self.pi)
    return cos_pi

  # Se crea el método para calcular tan(pi).
  def calcular_tan_pi(self):
    tan_pi = math.tan(self.pi)
    return tan_pi